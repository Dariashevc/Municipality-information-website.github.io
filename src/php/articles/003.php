<h3>What to Expect</h3>
<p>This year's festival will feature two main stages with live performances from local and regional artists, a dedicated family zone with carnival rides and activities for children, and over 80 local vendors offering food, crafts, and artisan goods.</p>

<p>New this year is a Cultural Pavilion showcasing the traditions, food, and performances of the city's diverse communities, as well as an outdoor film screening on the Saturday evening.</p>

<h3>Festival Schedule</h3>
<p>The festival runs daily from 11am to 10pm. Gates open at 10:30am on all three days. The main stage headliner will be announced in June — stay tuned to the city's social media channels for the reveal.</p>

<h3>Getting There</h3>
<p>Free shuttle buses will run from the downtown transit hub to Waterfront Park every 20 minutes throughout the festival. Parking is available at the fairgrounds lot with a suggested donation of \$5 going to the city's parks fund.</p>

