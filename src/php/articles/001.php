<p>The City is proud to announce the grand opening of the Downtown Community Centre, a state-of-the-art facility designed to serve residents of all ages and backgrounds.</p>

<p>Located in the heart of the city, the new centre spans over 30,000 square feet and includes a fully equipped fitness facility, an Olympic-size swimming pool, multipurpose event halls, and dedicated spaces for youth and senior programming.</p>

<h3>What's Inside</h3>
<p>The centre features a modern gymnasium, dance and yoga studios, a running track, and a childcare area. Community meeting rooms are available for local groups and organizations to book free of charge.</p>

<p>Mayor Jane Smith commented: \"This centre represents our commitment to building a healthier, more connected community. We want every resident to feel welcome here.\"</p>

<h3>Programs & Hours</h3>
<p>The centre will operate Monday through Friday from 6am to 10pm, and weekends from 8am to 8pm. A wide range of programs including swimming lessons, fitness classes, arts workshops, and after-school activities will launch throughout May.</p>

<p>Membership options are available for individuals, families, and seniors, with subsidized rates for low-income residents. Drop-in passes are also available at the front desk.</p>

<h3>How to Register</h3>
<p>Residents can register for programs online through the city website or in person at the centre starting May 5. For more information, contact the Parks & Recreation department at recreation@city.com.</p>
