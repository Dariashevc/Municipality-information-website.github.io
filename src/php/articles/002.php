<p>The City is launching its most ambitious road renewal initiative in over a decade, with construction crews set to begin work on several key corridors across the municipality this spring.</p>

<p>The project, budgeted at \$14.2 million, will address years of deferred maintenance on some of the city's most heavily trafficked roads, improving safety and reducing long-term repair costs.</p>

<h3>Which Roads Are Affected</h3>
<p>Phase one of the renewal project will focus on the following corridors:</p>
<ul>
    <li>Main Street from 1st Avenue to 12th Avenue</li>
    <li>Riverside Drive between the bridge and the community centre</li>
    <li>Northgate Boulevard from Highway 2 to Industrial Road</li>
</ul>

<p>Work will include full asphalt resurfacing, sidewalk repairs, curb and gutter replacement, and upgraded accessible crossings at key intersections.</p>

<h3>Construction Schedule</h3>
<p>Construction is expected to begin the week of May 12 and run through August 2026, weather permitting. Work will be scheduled during off-peak hours where possible to minimize disruption to daily commuters.</p>

<p>Detour routes will be clearly marked and updated on the city's online traffic map throughout the project.</p>

<h3>Minimizing Disruption</h3>
<p>The city has worked closely with local businesses and transit operators to plan construction phases that limit impact on commercial activity. Parking and access to all businesses will be maintained throughout the project.</p>

<p>Residents are encouraged to sign up for construction updates by email through the city website. For questions, contact the Public Works department at publicworks@city.com.</p>
